#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t	c;

	c = 0;
	while ((s1[c] && s2[c]) != '\0' && s1[c] == s2[c])
		c++;
	return ((s1[c] - s2[c]));
}
